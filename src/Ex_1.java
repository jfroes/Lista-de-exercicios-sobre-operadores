public class Ex_1 {

//    Declare duas variáveis inteiras e exiba a soma, subtração, multiplicação e divisão.

    public static void main(String[] args) {
        int n1 = 7;
        int n2 = 19;

        System.out.println(n1 + " + "  + n2 + " = " + (n1 + n2));
        System.out.println(n1 + " - "  + n2 + " = " +  (n1 -  n2));
        System.out.println(n1 + " * "  + n2 + " = " + (n1 * n2));
        System.out.println(n1 + " / "  + n2 + " = " + (n1 / n2));
    }
}
